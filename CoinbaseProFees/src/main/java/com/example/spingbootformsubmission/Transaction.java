package com.example.spingbootformsubmission;

public class Transaction {
	
	private String type;
	private double amount;
	
	public String getType() {
		return type;
	}
	
	public double getAmount() {
		return amount;
	}
	
	public void setType(String t) {
		type = t;
	}
	
	public void setAmount(double a) {
		amount = a;
	}
}
